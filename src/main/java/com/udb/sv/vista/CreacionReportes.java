    private void btngenerar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngenerar2ActionPerformed
        // TODO add your handling code here:
        int id = obtId();
        try {
            Conexion con = new Conexion();
            Connection conn = con.getConn();
            
            JasperReport resporte = null;
            String path = "src/main/java/com/udb/sv/reportes/Jugadores_por_equipo.jasper";
            
            Map parametro = new HashMap();
            parametro.put("id_equi", id);
            
            resporte = (JasperReport) JRLoader.loadObjectFromFile(path);
            
            JasperPrint jprint = JasperFillManager.fillReport(resporte, parametro, conn);
            
            
            JasperViewer view = new JasperViewer(jprint, false);
            view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            view.setVisible(true);
        } catch (Exception ex) {
            Logger.getLogger(CreacionReportes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btngenerar2ActionPerformed

    private void btnExcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcelActionPerformed
        try {
            Conexion con = new Conexion();
            Connection conn = con.getConn();
            
            JasperReport reporte = (JasperReport) JRLoader.loadObject(new File("/home/rigoberto/NetBeansProjects/reportes/src/main/java/com/udb/sv/reportes/Equipos.jasper"));
            
            JasperPrint jprint = JasperFillManager.fillReport(reporte, null, conn);
            
            JRXlsxExporter exporter = new JRXlsxExporter();
            exporter.setExporterInput(new SimpleExporterInput(jprint));
            exporter.setExporterOutput(new SimpleOutputStreamExporterOutput(new File("/home/rigoberto/Equipos.xlsx")));
            SimpleXlsxReportConfiguration config = new SimpleXlsxReportConfiguration();
            config.setDetectCellType(true);
            config.setCollapseRowSpan(false);
            exporter.setConfiguration(config);
            exporter.exportReport();
            JOptionPane.showMessageDialog(null, "Reporte creado satisfactoriamente");
            
        } catch (Exception e) {
            Logger.getLogger(CreacionReportes.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnExcelActionPerformed

